    private void jButton18ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton18ActionPerformed
        
        String answer;
        snum = Double.parseDouble(jtxtDisplay.getTeaxt());
        if (operations == "+")
        {
            res = fnum + snum;
            answer = String.format("%.0f",res);
            jtxtDislpay.setText(answer);
        }  
           else if (operations == "-")
        {
            res = fnum - snum;
            answer = String.format("%.0f",res);
            jtxtDislpay.setText(answer);
        }
        
          else if (operations == "/")
        {
            res = fnum / snum;
            answer = String.format("%.0f",res);
            jtxtDislpay.setText(answer);
        }
        
          else if (operations == "*")
        {
            res = fnum * snum;
            answer = String.format("%.0f",res);
            jtxtDislpay.setText(answer);
        }
    }//GEN-LAST:event_jButton18ActionPerformed

